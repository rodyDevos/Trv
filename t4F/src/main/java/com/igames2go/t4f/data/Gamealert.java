package com.igames2go.t4f.data;

public class Gamealert{
   	private String alertimage;
   	private String alertoption;
   	private String alerttext;
   	private String alerttitle;

 	public String getAlertimage(){
		return this.alertimage;
	}
	public void setAlertimage(String alertimage){
		this.alertimage = alertimage;
	}
 	public String getAlertoption(){
		return this.alertoption;
	}
	public void setAlertoption(String alertoption){
		this.alertoption = alertoption;
	}
 	public String getAlerttext(){
		return this.alerttext;
	}
	public void setAlerttext(String alerttext){
		this.alerttext = alerttext;
	}
 	public String getAlerttitle(){
		return this.alerttitle;
	}
	public void setAlerttitle(String alerttitle){
		this.alerttitle = alerttitle;
	}
}
